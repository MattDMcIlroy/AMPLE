function [Ib] = IbMatrixCreator(y)

%Generatres the Ib matrix
%--------------------------------------------------------------------------
% Author: Matthew McIlroy
% Date:   18/11/2022
% Description:
% Function to create the Ib matrix used in formulating the partial
% derivatives
%
%--------------------------------------------------------------------------
% [Ib] = IbMatrixCreator(y)
%--------------------------------------------------------------------------
% Input(s):
% y      - inverse of the second order tensor
%--------------------------------------------------------------------------
% Ouput(s);
% Ib     - Ib Matrix
%--------------------------------------------------------------------------
% See also:
%--------------------------------------------------------------------------
Ib=zeros(6);
Ib(1)  = y(1)^2;
Ib(2)  = y(2)^2;
Ib(3)  = y(7)^2;
Ib(4)  = y(1)*y(2);
Ib(5)  = y(2)*y(7);
Ib(6)  = y(1)*y(7);
Ib(7)  = y(2)^2;
Ib(8)  = y(5)^2;
Ib(9)  = y(6)^2;
Ib(10) = y(5)*y(2);
Ib(11) = y(5)*y(6);
Ib(12) = y(2)*y(6);
Ib(13) = y(7)^2;
Ib(14) = y(6)^2;
Ib(15) = y(9)^2;
Ib(16) = y(6)*y(7);
Ib(17) = y(9)*y(6);
Ib(18) = y(9)*y(7);
Ib(19) = y(1)*y(2);
Ib(20) = y(5)*y(2);
Ib(21) = y(6)*y(7);
Ib(22) = (y(1)*y(5)+y(2)^2)/2;
Ib(23) = (y(2)*y(6)+y(5)*y(7))/2;
Ib(24) = (y(1)*y(6)+y(2)*y(7))/2;
Ib(25) = y(2)*y(7);
Ib(26) = y(5)*y(6);
Ib(27) = y(9)*y(6);
Ib(28) = (y(2)*y(6)+y(5)*y(7))/2;
Ib(29) = (y(9)*y(5)+y(6)^2)/2;
Ib(30) = (y(9)*y(2)+y(6)*y(7))/2;
Ib(31) = y(1)*y(7);
Ib(32) = y(2)*y(6);
Ib(33) = y(9)*y(7);
Ib(34) = (y(1)*y(6)+y(2)*y(7))/2;
Ib(35) = (y(9)*y(2)+y(6)*y(7))/2;
Ib(36) = (y(9)*y(1)+y(7)^2)/2;
end